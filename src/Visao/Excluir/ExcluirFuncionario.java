    private void comboFuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboFuActionPerformed
        Connection con = Conexao.AbrirConexao();
        FuncionarioDAO sql = new FuncionarioDAO(con);
        List<Funcionario> lista = new ArrayList<>();
        String nome = comboFu.getSelectedItem().toString();
        
        lista = sql.ConsultarCodigoFuncionario(nome);
        
        for (Funcionario b : lista) {
            int a = b.getIdFuncionario();
            idfu.setText("" + a);
        }
        
        Conexao.FecharConexao(con);
    }//GEN-LAST:event_comboFuActionPerformed

