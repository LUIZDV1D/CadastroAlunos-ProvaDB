    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String codigo = idfu.getText();
        String nome = comboFu.getSelectedItem().toString();
        int n = comboFu.getSelectedIndex();
        
        
        Connection con = Conexao.AbrirConexao();
        FuncionarioDAO sql = new FuncionarioDAO(con);
        Funcionario a = new Funcionario();
        
        if ("1".equals(codigo)) {
           JOptionPane.showMessageDialog(null, "Este funcionário não pode ser excluido",
                    "Cadastro de Alunos", JOptionPane.WARNING_MESSAGE);
            idfu.setText(""); 
            comboFu.setSelectedIndex(0);
        } else {
        
            if (n == 0) {
                JOptionPane.showMessageDialog(null, "Nenhum nome selecionado",
                        "Cadastro de Alunos", JOptionPane.WARNING_MESSAGE);
                idfu.setText("");
            } else {

                int b = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir"
                        + "\n ( " + codigo + " ) ( " + nome + " ) ", "Cadastro de Alunos",
                        JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (b == 0) {
                        int cod = Integer.parseInt(codigo);
                        a.setNome(nome);
                        a.setIdFuncionario(cod);
                        sql.Excluir_Funcionario(a);
                        Conexao.FecharConexao(con);

                        JOptionPane.showMessageDialog(null, "Exluido com sucesso",
                            "Cadastro de Alunos", JOptionPane.WARNING_MESSAGE);

                        dispose();

                        new Menu().setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

