    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        
        String codigo = codA.getText();
        String nome = nomeA.getText();
        String idade = idadeA.getText();
        String nomePa = nomeP.getText();
        String nomeMa = nomeM.getText();
        String Cid = cidadeA.getText();
        String r = ruaA.getText();

        if (nome.equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio",
                "Cadastro de Alunos", JOptionPane.WARNING_MESSAGE);
        } else {
            
            Connection con = Conexao.AbrirConexao();
            AlunoDAO sql = new AlunoDAO(con);
            int cod = Integer.parseInt(codigo);
            int ida = Integer.parseInt(idade);
            
            Aluno a = new Aluno();

            a.setNome(nome);
            a.setIdade(ida);
            a.setNpai(nomePa);
            a.setNmae(nomeMa);
            a.setCidade(Cid);
            a.setRua(r);
            a.setIdaluno(cod);
            
            
            sql.Alterar_Aluno(a);
            
            Conexao.FecharConexao(con);

            nomeA.setText("");
            idadeA.setText("");
            cidadeA.setText("");
            nomeP.setText("");
            nomeM.setText("");
            ruaA.setText("");
            codA.setText("");

            JOptionPane.showMessageDialog(null, "Informações atualizadas",
                "Cadastro de Alunos", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

