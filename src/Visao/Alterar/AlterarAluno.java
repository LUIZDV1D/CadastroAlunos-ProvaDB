    private void InserirDados(int cod) {
        Connection con = Conexao.AbrirConexao();
        AlunoDAO sql = new AlunoDAO(con);
        List<Aluno> lista = new ArrayList<>();
        lista = sql.CapturarAluno(cod);
        
        for (Aluno a : lista) {
            codA.setText("" + a.getIdaluno());
            nomeA.setText("" + a.getNome());
            idadeA.setText("" + a.getIdade());
            nomeP.setText("" + a.getNpai());
            nomeM.setText("" + a.getNmae());
            cidadeA.setText("" + a.getCidade());
            ruaA.setText("" + a.getRua());
        }
        
        Conexao.FecharConexao(con);
    }

