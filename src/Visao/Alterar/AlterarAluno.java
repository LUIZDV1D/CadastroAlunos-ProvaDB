    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        String c = codp.getText();

        Connection con = Conexao.AbrirConexao();
        AlunoDAO sql = new AlunoDAO(con);

        int cod = Integer.parseInt(c);
        if (sql.testar_Aluno(cod) == false) {
            JOptionPane.showMessageDialog(null, "Código não encontrado no Banco",
                "Cadastro de Alunos", JOptionPane.ERROR_MESSAGE);
        }

        if (c.equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um Código para Atualizar",
                "Cadastro de Alunos", JOptionPane.WARNING_MESSAGE);
        }

        codA.setText("");
        nomeA.setText("");
        idadeA.setText("");
        cidadeA.setText("");
        nomeP.setText("");
        nomeM.setText("");
        ruaA.setText("");

        InserirDados(cod);
        codp.setText("");

    }//GEN-LAST:event_btnOkActionPerformed

